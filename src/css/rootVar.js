export function selectDefault() {
  document.documentElement.style.setProperty(
    "--header-background-color",
    "#292323"
  );
  document.documentElement.style.setProperty(
    "--editor-background-color",
    "#889e86"
  );
  document.documentElement.style.setProperty("--button-hover-color", "#889e86");
  document.documentElement.style.setProperty("--previewer-text-color", "white");
  document.documentElement.style.setProperty(
    "--panel-heading-text-color",
    "#292323"
  );
  document.documentElement.style.setProperty("--header-text-color", "white");
  document.documentElement.style.setProperty("--editor-text-color", "black");
  document.documentElement.style.setProperty("--border-color", "lightgray");
  document.documentElement.style.setProperty("--dropdown-text-color", "black");
  document.documentElement.style.setProperty("--image-url", "none");
}

